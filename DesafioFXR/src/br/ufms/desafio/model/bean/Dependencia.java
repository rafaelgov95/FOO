package br.ufms.desafio.model.bean;

import java.io.Serializable;

/**
 * Classe que mapeia a tabela Dependencia do banco de dados.
 *
 * @author Kleber Kruger
 */
public class Dependencia implements Serializable {

    private static final long serialVersionUID = 1L;

    private Responsavel responsavel;
    private Aluno aluno;
    private String parentesco;
    private Boolean ativo;

    /**
     * @return the responsavel
     */
    public Responsavel getResponsavel() {
        return responsavel;
    }

    /**
     * @param responsavel the responsavel to set
     */
    public void setResponsavel(Responsavel responsavel) {
        this.responsavel = responsavel;
    }

    /**
     * @return the aluno
     */
    public Aluno getAluno() {
        return aluno;
    }

    /**
     * @param aluno the aluno to set
     */
    public void setAluno(Aluno aluno) {
        this.aluno = aluno;
    }

    /**
     * @return the parentesco
     */
    public String getParentescoResponsavel() {
        return parentesco;
    }

    /**
     * @param parentesco the parentesco to set
     */
    public void setParentescoResponsavel(String parentesco) {
        this.parentesco = parentesco;
    }

    /**
     * @return the ativo
     */
    public Boolean getAtivo() {
        return ativo;
    }

    /**
     * @param ativo the ativo to set
     */
    public void setAtivo(Boolean ativo) {
        this.ativo = ativo;
    }

}
